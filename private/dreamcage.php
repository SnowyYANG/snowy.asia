<b id="a01">1.雪鸟的生活</b>
每天8点出门 每天18点到家 这是工作日的基调
9点的时候到写字楼 12点去便利店吃饭 13点返回写字楼 17点再离开
回到家打开游戏 玩了一整晚 到了0点就关了电脑睡觉
这样的生活 从学校毕业以来 已经过了几百天
工作的内容没有多少变化 便利店的伙食只有那么几种选择 游戏的内容几个季度更新一次
如果到了休息日 就玩上一整天的游戏
至于外出什么的 周围几个省份的城市都跑过 每个城市都是一排又一排的建筑 和来来往往的行人
偶尔去了一趟郊区 看过山水花鸟 感受过不同的气候温度 也只是细节的差别
我对这样的生活没有意外 认识的同龄人也大多过着这样的生活 活在人世间大概就是这样的吧
不过睡觉的时候 却能给我另一份体会
梦境
梦境变化无常 有着各种各样不同的气氛 有时觉得很温馨 有时觉得很古怪 有时能感到恐惧
看见过黑暗的山峦 也见过绮丽的草原 从未在世间见过的精灵 还有奇妙的声音
也许我在梦里意识穿行到了其他境界？
我不知道 现实生活多多少少有点重复和无聊 梦境却给了我并不单一的体验
还有就是 我每过一段时间 总能在梦里见到风君
每次见到他我都很高兴 我们可以说上各种各样的话题 偶尔能听到他演奏的音乐
现在想起来 我们已经认识十几年了 他从未离开过 我真的是觉得无比幸运

<b id="a02">2.什么在影响梦境</b>
梦见什么 总不是自己能决定的
有的时候心情很好 却会梦见可怕的事物
有的时候对风君思念至极 就是梦不见他
科学没有一致的说法 我曾经自己记录每一天的梦境 企图寻找规律
不过最终只能得出一个结论 梦境不是完全由我的意识做主的
学生时代可以午休 晚上的梦感觉比中午的梦更真实 我总希望可以晚上见到风君
不过风君大多在中午出现 有时才会在晚上出现
我在梦里想什么 似乎可以传达给风君 倒不如说 可以用意识对话
我可以听见 可以看见 可以有触觉 也可以感受到风君的想法
所以我那时就觉得 梦境大约是一种交流方式
最初我觉得他也是一个人 也在做梦
在我22岁的时候 也就是和他认识10年之后 我生了一场很重的病
在病中我有了这样的想法 风君不是人类
