<b id="a00">0.雪鸟与风君的共同幻梦</b>
梦里经常下雪 梦里经常飞行 所以自称自己是雪鸟
现实中使用的姓名 我觉得并不重要
他是我在梦中经常见到的一个男性
似乎比我大一两岁的样子
有一次梦里问他的名字 他回答我是风
一个字的名字太单调了 所以我在末尾加上了君字作为称呼
12岁那年 我第一次梦见了他
梦里我一个人走入了家附近的小山
在山间湖边见到了一个小屋子
我站在窗口 看见他在屋子里弹着钢琴
我听着钢琴的声音发呆
然后梦就醒了
在那之后十几年 几乎每天都能梦见他
不过有一年发生了可怕的事 在那之后我们没法保持每天的梦会了
