<b id="a0">0.我知道这是梦</b>
淡淡的光线斜入我的卧室，一种流通感从身体深处传来，我知道这是梦。
带着这样感觉的梦，我已经经历过十年了。
那个人又躲在什么高深的地方吧？
随着这样的感想，我径直走出窗户，在梦境中飞行起来。
其实，倒不如说是梦境引导着我进入下一个场景。
看着街道在身下移动，楼房在两边擦过，我不敢留意太多的细节。
