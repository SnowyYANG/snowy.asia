闭上眼睛什么都不看的话，梦境会非常缓慢的流逝，伴随着一阵阵的流通感。
我知道这份流通感的源头，那个人，名字的话，似乎是“风”，称呼的时候加上君字，风君。
终于，街道有了尽头，我看到了田野。
